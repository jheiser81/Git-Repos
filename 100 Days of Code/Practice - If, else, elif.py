# Write a program that asks the user for a number and then prints whether the number is positive, negative, or zero.
number = int(input("Please enter a number to check: "))
if number % 2 == 0:
    print("The number is even.")
elif number % 2 != 0:
    print("The number is odd.")
else:
    print("The number zero is neither even or odd.")

